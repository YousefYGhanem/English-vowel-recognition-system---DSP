%% cutting training audio files
function [y,f]=audioCut(a)
[y, f] = audioread(a,[2000 6600]);
y=abs(fft(y));
hd = design(fdesign.bandpass('N,Fc1,Fc2',10,200,3200,22000));
y = filter(hd,y);
end
%% cutting testing audio files and applying bandpass filter
function [y,f]=testCut(a)
[y, f] = audioread(a,[1800 6700]);
hd = design(fdesign.bandpass('N,Fc1,Fc2',10,200,3200,22000));
y = filter(hd,y);
end